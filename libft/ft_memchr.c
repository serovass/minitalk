#include <stdlib.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	unsigned char	ch;
	size_t			i;

	str = (unsigned char *)s;
	ch = (unsigned char)c;
	i = 0;
	while (n > 0)
	{
		if (str[i] == ch)
		{
			return (&str[i]);
		}
		n--;
		i++;
	}
	return (NULL);
}
